<h2>系统要求</h2>
<div class="product_requirement">
	<h3>Windows</h3>
	<ul>
		<li>Intel® Pentium® 4 或 AMD Athlon® 64 处理器</li>
		<li>带 Service Pack 3 或 Windows 7 的 Microsoft® Windows® XP</li>
		<li>1GB 内存（建议使用 2GB）</li>
		<li>1.6GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在可移动闪存设备上）</li>
		<li>1024x768 屏幕（建议使用 1280x800），16 位显卡</li>
		<li>DVD-ROM 驱动器</li>
		<li>需要使用 Adobe® Flash® Player 10 软件导出 SWF 文件</li>
		<li>Adobe Bridge 中的某些功能依赖于支持 DirectX 9 的图形卡（至少配备 64MB VRAM）</li>
		<li>该软件使用前需要激活。您必须具备宽带网络连接并完成注册，才能激活软件、验证订阅和访问在线服务。* 电话激活不可用。</li>
	</ul>
	<h3>Mac OS</h3>
	<ul>
		<li>Intel 多核处理器</li>
		<li>Mac OS X 10.6.8 或 10.7 版</li>
		<li>1GB 内存（建议使用 2GB）</li>
		<li>2.6GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在使用区分大小写的文件系统的卷或可移动闪存设备上）</li>
		<li>1024x768 屏幕（建议使用 1280x800），16 位显卡</li>
		<li>DVD-ROM 驱动器</li>
		<li>需要 Adobe Flash Player 10 软件用于导出 SWF 文件</li>
		<li>该软件使用前需要激活。您必须具备宽带网络连接并完成注册，才能激活软件、验证订阅和访问在线服务。* 电话激活不可用。</li>
	</ul>
</div>