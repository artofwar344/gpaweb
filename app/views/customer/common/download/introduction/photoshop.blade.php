	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/photoshop/special_3.jpg' }})">
		<strong>校色调色</strong>是photoshop中深具威力的功能之一，可方便快捷地对图像的颜色进行明暗、色编的调整和校正，也可在不同颜色进行切换以满足图像在不同领域如网页设计、印刷、多媒体等方面应用。
	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/photoshop/special_4.jpg' }})">
