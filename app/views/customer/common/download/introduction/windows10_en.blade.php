<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_5.jpg' }}" />
<p>&nbsp;&nbsp;强大的多任务管理能力是Win10的另一大亮点，它能让用户有条不紊地应对多重任务的复杂局面，对不同任务进行合理归类，优化窗口布局，轻松找到目标应用。虚拟桌面为用户提供更好、更方便、更有效的方式管理应用程序和组织自己的工作，每一个虚拟桌面都是独立的区域，用户可在不同的桌面自定义不同的应用程序组别，将工作和娱乐分开。</p>
