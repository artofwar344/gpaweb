<h2>微软® Windows 10 商品介绍</h2>
<h4>1. 硬件要求不高。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_1.jpg' }}" />
<p>&nbsp;&nbsp;与之前的系统相比，Win10对于硬件上的要求并没有提升。CPU 1GHz或更快、内存1GB(32位)或2GB(64位)、显卡带有WDDM驱动的DirectX9图形设备、显示器1024x600。按照这个标准，只要你的电脑可以轻松的运行XP，那么运行Win10也是毫无压力的。</p>
<br>
<h4>2. 性能提升,开机速度秒杀Win7。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_2.jpg' }}" />
<p>&nbsp;&nbsp;开机用时方面，Win10相比过去的操作系统时间更短，特别是Win7。其次，Win10 系统配合各项硬件加速模块，在执行诸如导航、游戏等方面工作时，会有明显的性能提升。另外，一直广受诟病的电源管理也在Win10中表现更加强大，大大延长了其设备的整体续航能力。综上所述，Win10在性能方面有了明显的提升。</p>
<br>
<h4>3. DX12游戏性能提升70%。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_3.jpg' }}" />
<p>&nbsp;&nbsp;早在今年上半年，intel就已经对DirectX 12的适配做了积极地准备。实际演示及测试中，在相同配置的情况下，DirectX 11运行游戏获得19帧的效果，画面顿卡严重，而在DirectX 12环境下运行，游戏帧数却骤然提升至33帧，足以保证画面的流畅度。实测结果也显示了DirectX 12相比较DirectX 11性能提升了至少70%左右。
<br>
&nbsp;&nbsp;游戏应用方面，不同Win10设备之间可以互相联机游戏;串流游戏功能让你在Win10电脑上畅玩Xbox One游戏。另外，Xbox应用录屏和截图功能，可以保存你最宝贵的游戏瞬间。特别的是，Win10中免费的Xbox Live会员还能让你和其他玩家轻松对战和交流。</p>
<br>
<h4>4. 传统开始菜单回归。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_4.jpg' }}" />
<p>&nbsp;&nbsp;Win10最大的亮点之一就是开始菜单整合了Win7传统开始菜单和Win8/Win8.1的Modern应用动态磁贴开始屏幕。这一改进主要是为了更加贴合用户的使用习惯，它可以让用户从桌面更快、更容易打开自己安装的应用程序。除此之外，Win10还可以自定义开始菜单的颜色和样式，让你的系统更加个性化。</p>
<br>
