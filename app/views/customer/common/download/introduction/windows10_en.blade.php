<h4>7. 全新的Edge浏览器。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_6.jpg' }}" />
<p>&nbsp;&nbsp;Edge浏览器是Win10中又一项重磅改进，不同于以往的IE系浏览器，Edge采用了全新渲染引擎，使得它在整体内存占用以及浏览速度上均有了大幅提升。最关键的是，Win10 Edge浏览器还为移动设备提供了更好的支持，能够让移动用户获得与PC机近似的使用体验。作为一款全新的浏览器，Edge浏览器增加了很多新的功能，如允许用户在网页上手写和做标记，并且可将这些内容分享给朋友;阅读列表可以让用户保存喜欢的文章，在方便的时候阅读;阅读模式可以为用户提供干净、无干扰的屏幕布局环境;内置数字助理Cortana小娜，可以为用户提供更多信息等。</p>
<br>
<h4>8. Continuum模式与平板自适应。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_7.jpg' }}" />
<p>&nbsp;&nbsp;Continuum模式是Win10的主要新功能之一，它可以让平板电脑、二合一设备以及变形本等设备用户更加方便地在平板模式和传统PC桌面模式下无缝自然切换。Windows10会自动感知设备运行模式的改变，并自动调整到最适合的模式，用户只需确认是否要改变模式即可。对于Win10平板模式来说，和传统桌面模式比较明显的区别在于开始屏幕、平板任务栏、应用窗口最大化显示等方面，这些都更适合平板电脑用户操作。</p>
<br>
<h4>9. Windows Hello保障安全。</h4>
<img src="{{ Config::get('app.asset_url') . 'images/customer/common/win10/win10_install_2_8.jpg' }}" />
<p>&nbsp;&nbsp;Win10提供了更加安全的方式保障用户信息。Windows Hello可以通过面部、指纹和虹膜识别三种方式进行用户身份验证，安全、精准、高效。需要注意的是，面部识别需要全新设备支持，比如英特尔3D“实感”相机，其他现有普通拍照设备均不符合这项功能的要求。不过，从官方给出的测试结果看，这种全新的面部识别不但速度极快，而且在安全性上绝对有保证，不必担心一个陌生人会拿着你的照片“蒙混过关”。</p>
