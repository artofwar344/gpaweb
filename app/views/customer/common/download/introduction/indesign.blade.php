		<strong>文字块和文字块中的文字具有神奇的填色和勾边功能</strong>，InDesign可给文字块中的文字填充实地色或渐变色，而且可给此文字勾任意粗的实地色或渐变色的边。同时，对此文字块也可给予实地色或渐变色的背景，文字块边框可勾任意粗的实地色或渐变色的边框。
	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/indesign/special_3.jpg' }})">
		<strong>文字块内的文字大小变化灵活</strong>，当我们进行编排时，往往会遇到想对某段文字块中的某些文字作一些特别强调，如大小、长短变化等等，InDesign就为您提供了这一方便功能。InDesign可让文字块内的文字在XY轴方向改变大小且可任意倾斜。
	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/indesign/special_4.jpg' }})">
		<strong>文字块的文字在间距控制上更自由</strong>，一般在排文时常常会遇到文字块最后一栏的最后一行不能与前面栏的最后一行平齐等等问题，这时可能就需要调整字距（Tracking）来实现了。InDesign的文字字距可简单的通过设定任意的数值来调整，非常快捷方便。
	</div>
</div>
