	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/fireworks/special_3.jpg' }})">
		<strong>高效集成</strong>- 导入 Photoshop (PSD) 文件, 导入时可保持分层的图层、图层效果和混合模式。 将 Fireworks (PNG) 文件保存回 Photoshop (PSD) 格式。 导入 Illustrator (AI) 文件, 导入时可保持包括图层、组和颜色信息在内的图形完整性。
	</div>
	<div class="special" style="background-image:url({{ Config::get('app.asset_url') . 'images/customer/common/fireworks/special_4.jpg' }})">
		<strong>支持多页</strong>- 使用新的页面板在单个文档 (PNG 文件) 中创建多个页面, 并在多个页面之间共享图层。 每个页面都可以包含自己的切片、图层、帧、动画、画布设置, 因而可在原型中方便地模拟网站流程。
	</div>
