<h2>系统要求</h2>
<div class="product_requirement">
	<h3>Windows</h3>
	<ul>
		<li>需要支持 64 位 Intel® Core™2 Duo 或 AMD Phenom® II 处理器</li>
		<li>Microsoft® Windows® 7 Service Pack 1（64 位）</li>
		<li>4 GB 的 RAM（建议分配 8 GB）</li>
		<li>用于安装的 4 GB 可用硬盘空间；安装过程中需要其它可用空间（不能安装在移动闪存存储设备上）</li>
		<li>预览文件和其它工作文件所需的其它磁盘空间（建议分配 10 GB）</li>
		<li>1280x900 显示器</li>
		<li>支持 OpenGL 2.0 的系统</li>
		<li>7200 RPM 硬盘（建议使用多个快速磁盘驱动器，首选配置了 RAID 0 的硬盘）</li>
		<li>符合 ASIO 协议或 Microsoft Windows Driver Model 的声卡</li>
		<li>与双层 DVD 兼容的 DVD-ROM 驱动器（用于刻录 DVD 的 DVD+-R 刻录机；用于创建蓝光光盘媒体的蓝光刻录机）</li>
		<li>QuickTime 功能需要的 QuickTime 7.6.6 软件</li>
		<li>可选：Adobe 认证的 GPU 卡，用于 GPU 加速性能</li>
		<li>在没有激活的情况下，此软件不会运行。激活软件、验证订阅和访问联机服务需要宽带 Internet 连接和注册。* 不提供电话激活。</li>
	</ul>
	<h3>Mac OS</h3>
	<ul>
		<li>支持 64 位多核 Intel 处理器</li>
		<li>Mac OS X v10.6.8 或 v10.7</li>
		<li>4 GB 的 RAM（建议分配 8 GB）</li>
		<li>用于安装的 4 GB 可用硬盘空间；安装过程中需要其它可用空间（不能安装在使用区分大小写的文件系统卷或移动闪存存储设备上）</li>
		<li>预览文件和其它工作文件所需的其它磁盘空间（建议分配 10 GB）</li>
		<li>1280x900 显示器</li>
		<li>7200 RPM 硬盘（建议使用多个快速磁盘驱动器，首选配置了 RAID 0 的硬盘）</li>
		<li>支持 OpenGL 2.0 的系统</li>
		<li>与双层 DVD 兼容的 DVD-ROM 驱动器（用于刻录 DVD 的 SuperDrive 刻录机；用于创建蓝光光盘媒体的蓝光刻录机）</li>
		<li>QuickTime 功能需要的 QuickTime 7.6.6 软件</li>
		<li>可选：Adobe 认证的 GPU 卡，用于 GPU 加速性能</li>
		<li>在没有激活的情况下，此软件不会运行。激活软件、验证订阅和访问联机服务需要宽带 Internet 连接和注册。* 不提供电话激活。</li>
	</ul>
</div>