<h2>系统要求</h2>
<div class="product_requirement">
	<h3>Windows</h3>
	<ul>
		<li>Intel® Pentium® 4 或 AMD Athlon® 64 处理器</li>
		<li>Microsoft® Windows XP（装有 Service Pack 3）或 Windows® 7（装有 Service Pack 1） </li>
		<li>32位需要 1GB 内存（推荐 3GB）；64 位需要 2GB 内存（推荐 8GB）</li>
		<li>2GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在可移动闪存设备上）</li>
		<li>1024x768 屏幕（推荐 1280x800），16 位显卡</li>
		<li>兼容双层 DVD 的 DVD-ROM 驱动器</li>
		<li>Adobe® Bridge 中的某些功能依赖于支持 DirectX 9 的图形卡（至少配备 64MB VRAM）</li>
		<li>该软件使用前需要激活。您必须具备宽带网络连接并完成注册，才能激活软件、验证订阅和访问在线服务。† 电话激活不可用。</li>
	</ul>
	<h3>Mac OS</h3>
	<ul>
		<li>Intel 多核处理器（支持 64 位）</li>
		<li>Mac OS X 10.6.8 或 10.7 版</li>
		<li>2GB 内存（推荐 8GB）</li>
		<li>2GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在使用区分大小写的文件系统的卷或可移动闪存设备上）</li>
		<li>1024x768 屏幕（推荐 1280x800），配备符合条件的硬件加速 OpenGL 图形卡、16 位颜色和 256MB VRAM</li>
		<li>兼容双层 DVD 的 DVD-ROM 驱动器</li>
		<li>该软件使用前需要激活。软件激活、订阅验证和访问在线服务需要宽带网络连接和注册。† 电话激活不可用。</li>
	</ul>
</div>