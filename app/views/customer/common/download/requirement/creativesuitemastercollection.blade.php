<h2>系统要求</h2>
<div class="product_requirement">
	<h3>Windows</h3>
	<ul>
		<li>Intel® Pentium® 4 或 AMD Athlon® 64 处理器</li>
		<li>Microsoft® Windows XP*（装有 Service Pack 3）或 Windows® 7（装有 Service Pack 1）</li>
		<li>32位需要 1GB 内存（推荐 3GB）；64 位需要 2GB 内存（推荐 8GB）</li>
		<li>14.5GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在可移动闪存设备上）</li>
		<li>分辨率1280x900 和1680x1050，16 位颜色和 256 MB 的显存。SpeedGrade 需要 1680x1050 显示器，建议使用第二台专业校準检视显示器</li>
		<li>支持 OpenGL 2.0 系统</li>
		<li>声卡兼容ASIO协议或Microsoft WDM / MME</li>
		<li>兼容双层 DVD 的 DVD-ROM 驱动器，蓝光刻录机制作Blu-ray光盘介质兼容的DVD-ROM驱动器</li>
		<li>Java™运行环境1.6（包含）</li>
		<li>Eclipse™ 3.7 (用于 Adobe Flash® Builder® 增效模组安装)；支援以下散发版本：适用于 Java EE 和 Java 开发人员的 Eclipse IDE、Eclipse Classic、适用于 PHP 开发人员的 Eclipse</li>
		<li>QuickTime 功能、多媒体和 HTML5 媒体播放需要 QuickTime 7.6.6 软体SpeedGrade 需要专用的 GPU 卡 (以在 SpeedGrade 中提供最佳效能，以及支援 Adobe Premiere Pro 和 After Effects 中的 GPU 加速功能：建议使用 NVIDIA Quadro 4000、5000 或 6000 或其他 Adobe 认证 GPU 卡，并具备至少 1GB VRAM)；若需最新的支援</li>
		<li>图形卡清单，请造访 www.adobe.com/products/premiere/extend.html</li>
		<li>选购：Tangent CP200 系列或 Tangent Wave 控制介面 (适用于 SpeedGrade)</li>
		<li>选购：针对 SDI 输出，SpeedGrade 需要 NVIDIA Quadro SDI 输出卡</li>
		<li>选购：适用于视讯产品的 7200 RPM 硬碟 (建议使用多部快速磁碟机，最好已设定 RAID 0)</li>
		<li>该软件使用前需要激活。软件激活、订阅验证和访问在线服务需要宽带网络连接和注册。† 电话激活不可用。</li>
	</ul>
	<h3>Mac OS</h3>
	<ul>
		<li>Intel 多核处理器（支持 64 位）</li>
		<li>Mac OS X 10.6.8 或 10.7 版</li>
		<li>4GB 内存（推荐 8MB）</li>
		<li>15.5GB 可用硬盘空间用于安装；安装过程中需要额外的可用空间（无法安装在使用区分大小写的文件系统的卷或可移动闪存设备上）</li>
		<li>磁碟快取、预览档案和其他工作档案需要额外的磁碟空间 (建议使用 10GB)</li>
		<li>1280x900 显示器，具 16 位元色彩及 512MB 的 VRAM；Adobe SpeedGrade 需要 1680x1050 显示器，建议使用第二台专业校準检视显示器</li>
		<li>支持 OpenGL 2.0 系统</li>
		<li>兼容双层 DVD 的 DVD-ROM 驱动器</li>
		<li>Java Runtime Environment 1.6</li>
		<li>Eclipse 3.7 Cocoa 版本 (用于 Adobe Flash Builder 增效模组安装)；支援以下散发版本：适用于 Java EE 和 Java 开发人员的 Eclipse IDE、Eclipse Classic、适用于 PHP 开发人员的 Eclipse</li>
		<li>QuickTime 功能、多媒体和 HTML5 媒体播放需要 QuickTime 7.6.6 软体</li>
		<li>建议使用专用的 GPU 卡 (以在 SpeedGrade 中提供最佳效能，以及支援 Adobe Premiere Pro 和 After Effects 中的 GPU 加速功能：建议使用 NVIDIA Quadro 4000 或其他 Adobe 认证 GPU 卡，并具备至少 1GB VRAM)；若需最新的支援图形卡清单，请造访 www.adobe.com/products/premiere/extend.html</li>
		<li>选购：Tangent CP200 系列或 Tangent Wave 控制介面 (适用于 SpeedGrade)</li>
		<li>选购：适用于视讯產品的 7200 RPM 硬碟 (建议使用多部快速磁碟机，最好已设定 RAID 0)</li>
		<li>该软件使用前需要激活。软件激活、订阅验证和访问在线服务需要宽带网络连接和注册。† 电话激活不可用。</li>
	</ul>
</div>